class Movie {
	constructor(
		coverPicture,
		title,
		kind,
		dated,
		rated,
		score,
		director,
		actors,
	) {
		this.coverPicture = coverPicture;
		this.title = title;
		this.kind = kind;
		this.dated = dated;
		this.rated = rated;
		this.score = score;
		this.director = director;
		this.actors = actors;
	}
}
